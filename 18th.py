
# String manipulation operations
name = "John Doe"

# Slicing
print("First name:", name[:5])
print("Last name:", name[6:])

# Concatenation
full_name = name[:5] + " " + name[6:]
print("Full name:", full_name)

# Formatting
age = 25
print("My name is {} and I am {} years old.".format(full_name, age))
